function isEven(num){
    if( num % 2 === 0){
        return true;
    }
    else{
        return false;
    }
}

console.log(isEven(5));
console.log(isEven(150));

function isOdd(num){
    if( num % 2 === 1){
        return true;
    }

    return false;
}

console.log(isOdd(14));
console.log(isOdd(35));